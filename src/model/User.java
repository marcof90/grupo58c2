package model;
public class User {
    private int status;
    private String name;
    // Getters
    public int getStatus() {
        return status;
    }
    public String getName() {
        return name;
    }
    // Setters
    public void setName(String name) {
        this.name = name;
    }
    public void setStatus(int status) {
        this.status = status;
    }

}
